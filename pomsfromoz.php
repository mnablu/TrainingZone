<h2>Poms From Oz</h2>
<p><img src="musos/Poms.jpg" width="800" height="591" alt=""/></p>
<p>Poms   from Oz are Judy, Kathleen and Alan Pomeroy, a family of solo vocalists   from North Queensland, Australia. All are dinky di Oz with Judy and   Kathy being descendents of First Fleeters. <br>
  <br>
Their tight harmony and vocal strength bring a fresh feel   to original, contemporary and traditional works, uncovering a rich   tapestry of songs gathered in their travels from around the world. Most   songs are a cappella but sometimes accompanied by guitar and/or   harmonica. Moods in their songs range from downright to real tear   jerkers and audience participation is encouraged. Their sound is   distinctive, as Judy arranges all the songs specifically for their   voices. <br>
<br>
A tast brew of Oz songs and Oz voices, with a dash of   guitar and harmonica. Open a nice bottle of red , sit back and enjoy.                                              For further information or bookings contact the Music Centre 4724 2086 or email admin@townsvillemusic.org.au </p>
