<p>The   concept of the Camerata Singers originated in the 1980s when Susan   Grinsell, who was teaching voice at James Cook University realised that   ensemble singing was sadly lacking for tertiary music students. Since   then, Susan, along with repetiteur Carol Dall�Osto, has strived to bring   quality ensemble singing to the wider community by performing at events   from the Ingham Italian Festival through to Cotter�s Markets in the   mall. <br>
  <br>
The Camerata Singers comprises a group of trained singers,   both male and female, from age 18 and over who strive to provide quality   performances in a range of styles from classical through to A cappella   and contemporary.  A number of members also regularly participate in   local music theatre and theatre productions.                                             For further information or bookings contact the Music Centre 4724 2086</p>
